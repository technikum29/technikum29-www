<h2>FAQ: Frequently Asked Questions</h2>
<p>Wir bekommen derzeit häufig ähnliche Fragen gestellt, die wir hier veröffentlichen
und auch gleich beantworten möchten.

<h3>Fragen bezüglich der Corona-Pandemie (COVID-19)</h3>

<dl class="faq">

<dt>
Hat das Museum während der laufenden Corona-Pandemie geöffnet?
<dd>
Wir orientieren uns an den offiziellen Regeln des Landes Hessen. Diese erlauben
größere Menschenansammlungen in geschlossenen Räumen nur mit Mund-/Nasenabdeckung
(Atemmaske). Auf diese Weise haben wir <a href="/blog/2020-07-07-Nachlese-Helfertag.php">einen
Helfertag im Juli 2020 veranstaltet</a>. Ähnliche Formate möchten wir auch in naher
Zukunft anbieten. Generelle Führungen wird es aber in der nächsten Zeit nicht geben.
Aktuelles entnehmen Sie stets <a href="/de/termine.php">unserer Terminseite</a>.

<dt>
Ist das Museum durch die Corona-bedingte Schließung in finanzielle Schwierigkeiten
gekommen?  <br> Ist die Existenz gesichert?
<dd>
Der durch die Covid-19-Pandemie verursachte Lockdown in ganz Deutschland ist am
technikum29 recht spurlos vorbeigegangen. Das liegt daran, weil wir kein
»Geschäftsmodell« haben, welches auf Eintrittsgeldern basiert. Die Eintrittsgelder,
um die wir bei Führungen bitten, sind nicht kostendeckend oder gar zur Absicht der
Gewinnerzielung bemessen. Sie sollen in erster Linie die Unkosten decken, die durch
eine Führung selber entsteht, etwa Heizkosten oder Kosten für Spesen.
Unabhängig von Corona suchen wir allerdings immer
<a href="/de/unterstuetzen.php">UnterstützerInnen oder SpenderInnen</a>.

<dt>Sind ältere Menschen im Museum besonders gefährdet?
<dd>
Ältere Menschen werden während Corona öfters der »Risikogruppe« zugeordnet. Wir tun
bei Öffnungen alles uns mögliche, um einen gefahrlosen Aufenthalt zu ermöglichen.
Dazu gehört eine maximale Belüftung der Ausstellungsflächens sowie der Aufenthalt
auf den Außenflächen, soweit es das Wetter erlaubt. Da wir eine Erdgeschosslage
haben, ist der Wechsel zwischen Innen und Außen jederzeit und leicht möglich.

<dt>Gibt es nachvollziehbare Listen über die Museumsbesucher mit Datum und Zeit?
<dd>
Ja. Bedingt durch die Corona-Pandemie ist der Museumsbesuch nur nach Anmeldung
erlaubt. Für den Fall, dass Besucher spontan (aber abgesprochen) weitere uns
nicht bekannte Gäste mitbringen, liegen Listen aus, auf denen wir um Eintragung
von Name, Adressdaten, Ankunfts- und Abfahrtzeit bitten. Wir heben diese Bewegungsdaten
nur für einige Wochen auf, um evventuelle Übertragungsketten nachvollziehen zu
können und anfragende zuständige Behörden unterstützen zu können. Die erhobenen
Bewegungsdaten werden nach einem Monat vernichtet.

<dt>Ist es im Museum möglich, den empfohlenen Sicherheitsabstand von 1.5 bis 2
Metern einzuhalten?
<dd>
Im Allgemeinen schon. Die Ausstellungsfläche im Erdgeschoss bietet genügend
Bewegungsfläche, um sich ausreichend aus dem Weg zu gehen. Es gibt drei Ausgänge
in den Garten sowie ein halbes Dutzend Fenster, die Frischluft garantieren.
In jedem Kellerraum ist der Sicherheitsabstand mit weniger als drei Personen
immer garantiert. Außerdem lassen sich alle Kellerräume (Souterrain) bis auf den
Saferaum mit mehreren Fenster belüften.

<dt>Wie lautet das Hygienekonzept des Museums?
<dd>
Ergänzend zu den oben erläuterten Maßnahmen stehen im Museum auch mehrere obligatorische
Handdesinfektionsspender zur Verfügung. Es gibt mehrere Waschräume mit Toiletten,
die das häufige Händewaschen ermöglichen sollen. Besuchern legen wir allgemein ans
Herz, zum Händetrocknen eigene Handtücher mitzubringen, da wir zum Klimaschutz
keine Einmalhandtücher einsetzen möchten. Unsere Handtücher werden regelmäßig
gewaschen. Wir können uns aber derzeit kein Reinigungspersonal leisten, sodass die
Reinigung unserer Räumlichkeiten von uns übernommen wird.

<dt>Was ist die minimale Anzahl Personen, denen ich beim Besuch des Museums
begegnen muss?
<dd>
Wir empfehlen während der Corona-Pandemie die Anreise mit dem Auto. Das Museum
verfügt über eine große Anzahl eigener Parkplätze und ist gut an das Autobahnnetz
angebunden. Im Museum ist immer mindestens einE AnsprechpartnerIn anwesend, der/die das
Gebäude öffnet und Sie als Gast in Empfang nimmt. Wir empfehlen, im Vorfeld alle
Fragen per E-Mail oder telefonisch zu beantworten, um die Kontaktzeiten zu
minimieren.

<dt>Ist es unter Hygiene-Gesichtspunkten gefährlich, Geräte im Museum anzufassen?
<dd>
Die Studienlage, wie lange sich das Corona-Virus auf Oberflächen hält, ist äußerst
unterschiedlich. Das SARS-Virus kann allerdings durch einfache Desinfektionsmittel
wirkungsvoll abgetötet werden. Besucher mit Bedenken empfehlen wir daher den Einsatz
von Desinfektionsmittel. Dies unterstützt auch unsere Bemühungen, das Museum und seine
Exponate in einem reinlichen Zustand zu halten. Bitte sprechen Sie immer mit uns,
bevor Sie Exponate reinigen. Nicht alle Reinigungsmittel sind für alle Oberflächen
geeignet.
Das dauerhafte Tragen von nicht atmungsaktiven Schutzhandschuhen wird aus hygienischen
Gründen für den/die TrägerIn im Allgemeinen nicht empfohlen.
<br>
Generell ist das Museum während der Pandemie wenig frequentiert, sodass ein Überleben
des Virus zwischen verschiedenen Besuchen, die Tage auseinander liegen, so gut wie
ausgeschlossen ist.

<dt>Wie kann ich zur Bewältigung der Corona-Situation im Museum beitragen?
<dd>
Wir freuen uns über jede Initiativen zur Ergreifung von Maßnahmen, die geeignet erscheinen.
Wer beispielsweise Mittel des Hygienebedarfs, für Bad oder Küche mitbringen möchte, ist
herzlich eingeladen. Um vorherige Koordination per E-Mail wird gebeten.

</dl>


