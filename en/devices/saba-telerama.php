      <p>The <b>Schmitt lenses</b>, as used in the Saba Telerama projection TV-set.
  </div>
</div>

<p>
         The CRT (6 cm diameter) is partly visible in the picture. The light coming from this tube reflects on to the concave mirror (on the floor). The reflected light is adjusted by a special lens (on the right of the box) and leaves the assembly, arriving at the projection screen. During this process, the original picture, about 4,5cm x 3,5cm, is scaled up to 123cm x 97,5cm on the projection screen. Consequently, the luminosity is reduced enormously. Consequently, this projection TV needed to be viewed in an almost totally shaded room.</p>
