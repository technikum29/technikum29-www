    <h2 id="itelex">Das i-Telex</h2>
    
	<div class="box center clear-after">
	    <img src="/shared/photos/kommunikationstechnik/i-telex.jpg" width="342" height="262" />
		<div class="bildtext"><b>Unsere i-Telex-Anlage</b><br/>Links die Netzwerkkarte zur Verbindung mit dem Internetrouter, in der Mitte zwei TW39-Steckkarten für 4 TW39-Schnittstellen
        und rechts daneben das Netzteilmodul, das die Elektronik versorgt und die benötigte Linienspannung von ca. 85 Volt liefert (einige Fernschaltgeräte benötigen diese hohe Spannung).
        Der Linienstrom bei bestehender Verbindung beträgt ca. 40 mA.</div>
	</div>
    <div><p>2007 wurde das öffentliche Fernschreibnetz in Deutschland abgeschaltet. Um ihre Fernschreibersammlungen trotzdem weiterbetreiben zu können, wurde von einigen
    Enthusiasten das i-Telex entwickelt.
    Darüber konnten nun Fernschreiber zunächst nur über das Telefonnetz und inzwischen auch über Internet miteinander kommunizieren. Ein zentraler Server setzt
    die im i-Telex-Netzwerk vergebenen Telexnummern in Internetadressen um, so dass empfangsbereite Fernschreiber weltweit von anderen i-Telex-Teilnehmern direkt angewählt werden können
    (ca. 300 Geräte weltweit).<br/>
    Lokal simuliert das i-Telex wahlweise eine TW39- oder die modernere ED1000-Vermittlungsstelle, an die Fernschreiber direkt angeschlossen werden.
    Die Anwahl eines anderen Teilnehmers funtioniert genau wie bei einem echten Fernschreibanschluss über die Wählscheibe. Auch die Behandlung von eingehenden Verbindungen mit dem automatischen
    Einschalten des Fernschreibers über das Fernschaltgerät funktioniert originalgetreu.
    Zusätzlich kann über das i-Telex auch eine Wählverbindung zuwischen den angeschlossenen lokalen Fernschreibern aufgebaut werden.</p>
    <p>Einige unserer Fernschreiber sind mit einer solchen i-Telex-Anlage verbunden, so dass die Besucher selbst den Verbindungsaufbau und die Nachrichtenübermittlung
    (über Tastatur oder von Lochstreifen) ausprobieren können.<br/>
    Im Endausbau sollen in unserer Ausstellung 6 funktionsfähige Fernschreiber an das i-Telex angeschlossen sein.</p>
    <p><a href="https://www.i-telex.net/" target="_blank">www.i-telex.net</a></p></div>
