    <h2>Fernschreibtechnik</h2>
    
    <div class="box center clear-after">
        <a class="popup" href="/shared/photos/kommunikationstechnik/siemens-t32.jpg" title="Siemens">
        <img src="/shared/photos/kommunikationstechnik/siemens-t32.jpg" alt="Siemens T32" width="326" height="312" /></a>
        <div class="bildtext"><b>Siemens Springschreiber T32</b><br/>(zum Vergrößern anklicken)</div>
    </div>

	<p>Der ratternde Fernschreiber bestimmte Jahrzehnte das Bild der schnellen Nachrichtenübermittlung. 1930 erstmals von Siemens & Halske vorgestellt, wurde der hier abgebildete T32 
    am 16. Oktober 1933 von der Deutschen Reichspost eingeführt. Die erste amtliche Verbindung bestand zwischen Berlin und Hamburg. Für das Fernschreibnetz mussten separate Leitungen verlegt werden.<br/>
    Von 21 Teilnehmern 1933 stieg die Zahl auf 1500 im Jahre 1939 und schließlich auf über 90.000 um 1975. Der mechanische Fernschreiber wurde nun auf elektronische umgestellt.<br>
    Die Blütenzeit der Mechanik in der Nachrichtentechnik bestand u.a. in den verschiedenen Fernschreibertypen. Der relativ seltene Springschreiber T32 wurde ab den 30er Jahren bis in die
    50er hergestellt. Es ist ein Streifenschreiber mit überschaubarem Aufwand aber ungeheuer stabil aufgebaut. Im Buch von F. Schiweck "Fernschreibtechnik" (1942) wird hervorgehoben:</p>
    <blockquote>"Der Fernschreiber ist heute eins der hervorragendsten Erzeugnisse der Feinwerktechnik, indem er in sich höchste Kunst der Getriebetechnik, Werkstoffbehandlung und Herstellung
    vereinigt. Seine Betriebssicherheit ist nach dem Urteil der Praxis außerordentlich groß".</blockquote>
    <p>In diesem voll mechanisiertem Zeitalter benötigten die "Büromaschinenmechaniker" ein gefühlvolles Händchen, gute Ohren um eventuelle Störgeräusche einordnen zu können und jede Menge
    Ölkännchen mit verschiedenen Ölsorten. In den 80er Jahren ist dieser Beruf schließlich ausgestorben.
    <p>
    Wir zeigen in der Ausstellung Fernschreiber unterschiedlichster Bauart (Blattschreiber und Streifenschreiber) von ca. 1940 bis 1960. Zwei weitere Fernschreiber sollen hinzukommen und
    den Zeitraum bis 1980 abdecken.</p>
    <br/>

    <h2 id="lorenz-lo15b">Lorenz Springschreiber Lo15b</h2>
		
	<div class="box center clear-after">
		<img src="/shared/photos/dummy.jpg" width="420" height="336" alt="Lorenz Lo15b" />		
		<div class="bildtext">
            <b>Lorenz Springschreiber Lo15b Bj. ca. 1960</b> 
		</div>
	</div>
    <div><p>Der Springschreiber Lo15b gehört zur zweiten Generation von Fernschreibern der Lo15-Serie, die von der Firma Lorenz seit 1930 als Lizenzbau des Teletype Model 15 gebaut wurden.
    Unser Modell stammt von ca. 1960 und war laut dem original erhaltenen Kennungsgeber noch bis 1983 bei der Firma Rudolf Hoffmann in Nürnberg im Einsatz.
    Die mechanischen Fernschreiber aus dieser Zeit waren bei regelmäßiger Wartung nahezu unverwüstlich und über viele Jahre im Einsatz.<br/>
    Der grundsätzliche mechanische Aufbau des Lo15 wurde über alle Gerätegenerationen kaum verändert. Wie auch die Siemens Fernschreiber wurde der Lo15 in den unterschiedlichsten
    Gehäuseformen angeboten. Unser Holzstandgehäuse befindet sich, wie der Fernschreiber selbst, in einem sehr guten Zustand.<p/>
    <p>Um Zeit und damit Geld bei der Übermittlung der Nachrichten zu sparen, besitzt der Lo15b einen integrierten 5-Kanal-Lochstreifenstanzer und -sender, um Nachrichten "offline"
    vorzustanzen und dann direkt vom Lochstreifen zusenden.
    Um Lochstreifen lokal bearbeiten zu können, war ein Fernschaltgerät (FSG) mit Lokalmodus nötig, das die erfoderliche Linienspannung zur Verfügung stellte. Der Lokalmodus war ein Zusatz,
    der exta bezahlt werden musste, der sich aber bei häufigem Gebrauch durch die gesparten Verbindungskosten rechnete.
    Das FSG diente ansonten der Anwahl anderer Teilnehmer (Wählscheibe) und schaltete bei eingehenden Verbindungen den Fernschreiber automatisch ein.</p></div>
