das zweite Corona-Jahr neigt sich dem Ende zu, und auch 2021 möchten wir allen Freunden und Helfern für Ihre Arbeit und Unterstützung danken. Wir wünschen euch, euren Familien und Lieben gesunde, ruhige Festtage und ein erfolgreiches Neues Jahr!

<h3>Was geschah 2021?</h3>

<p>Die anhaltende Pandemie hat auch 2021 nur wenige <strong>Führungen oder Helfer- und Bastlertage</strong> zugelassen. Unsere Gäste waren allerdings durchweg begeistert und füllten das Gästebuch mit Kommentaren und unser Sparschwein mit dem einen oder anderen Schein. Auch <strong>Organisatorische</strong> Fortschritte wie die Vereinsgründung wurden weiter verschoben. In den <strong>Depots</strong> wurde weiter systematisch aufgeräumt und transportiert. Wir mussten ein Depot räumen, sodass wir aus Mangel an Alternativen im Museumskeller einiges an Ausstellungsfläche zu Lagerfläche umgewandelt haben. Viele Geräte haben aber auch dank der kleinteiligen und ausdauernden Mitarbeit vieler Helfer und Kontakte neue Besitzer gefunden. Nach wie vor <a href="/blog/2020-09-03-Lagersuche.php">suchen wir bezahlbaren Lagerraum auf Zeit</a>. Wer immer etwas weiss - bitte melden!

