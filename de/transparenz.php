<h3>6. Personalstruktur</h3>
<p>Unsere Helfer arbeiten alle Ehrenamtlich. Viele Mitarbeiter kommen von weit her.
Derzeit besitzen nur wenige Personen eine Schließberechtigung für die Räumlichkeiten, daher
