		Figure 1 shows the keyboard with the ball-printing mechanism, the control console and the display panel. The keyboard is identical to that used in the IBM 029 card punch - its decoding mechanism is purely mechanic. The smallest configuration of the computer system had no memory expansion (front, left), no IBM 1132 printer, no removable disk drive and used paper tape as the main input/output medium. Since the paper tape system was no match for the rather powerful processor, most machines came with a lot more peripherals than this minimal configuration. The system could be expanded easily and thus could grow with the demands of its users.<br><br>
		
			The IBM 1132 printer is based in the IBM 407 tabulating machine which was developed in the 1950s. The 1132 features a simplified printing mechanism. Figure 2 shows the printer with its side panels removed. This is the view our visitors have on this rather archaic printing system. After restauration the printer looks like new. Visible on the front are the 120 solenoids which control the printing wheels - rock-solid technology of the 1950s and 1960s. 
			<br>The printer is capable of printing 80 lines per minute - not too impressive given the rather modern electronic control system and a result of the comparable low price of the overall system. Users demanding more printer throughput could also attach an IBM 1403 high-speed printer.

			<br><br>
			Figure 3 shows an 8k x 16 bit core memory stack (16 kB) with its associated control circuitry. The high density of this module is typical for IBMs SLT based devices of that time. IBM was far ahead of most of its competitors (not only in this respect). The matchbox shown visualizes the small size of this memory module. Most IBM 1130 systems only had one such module - the memory expansion of our system (left) is a rare device.
			<br><br>
			
			Our system also features several Facit paper tape devices (figure 4):
<br>			A paper tape winder type 4015, paper tape punch type 4060, paper tape reader type 4001, and punch control type 5104. Back then it was customary to have paper tape equipment connected to computers used in a scientific environment. Since these devices are very sturdy they were sometimes the last means in cases of major malfunctions of other input/output equipment. Unfortunately, it is not possible to start programs from paper tape - this requires an extra option.
 
		<br><br>
			Figure 5 shows additional output devices such as a plotter and a terminal. The COMPLOT DP-1 digital plotter shown is a fully transistorized high-speed plotter which was used instead of the much slower IBM plotter (which was basically a Calcomp 565 plotter). As simple as the plotter is from a mechanical and electronic point of view, it was extremely expensive at more than 18,000 DM in 1971 (about as expensive as two middle-class cars of that time). The profit-margin back than was quite remarkable.<br>
 
On the right a Tektronix 4006 graphic terminal can be seen. This device is capable of displaying text as well as graphics and was introduced in 1975. Instead of a bit-mapped display, a storage display tube was used since digital memory chips were prohibitively expensive back then. This display tube is based on the same principles of operation as contemporary storage oscilloscope tubes.<br>
 
The processing of large data sets required a tape drive such as our Ampex TMZ shown in figure 6. It was cheaper than comparable devices used in larger installations.<br><br>
 
