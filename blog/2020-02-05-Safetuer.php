<p>Diese Futtertür ist mit zwei kleinen einfachen Zylinderschlössern abgeschlossen, bei denen
es sich womöglich einfach nur um Hebelschlösser handelt, wie sie auch bei Briefkästen vorkommen.
Leider fehlen uns dafür die Schlüssel. Die Nahaufnahme zeigt ein solches Schloss, im 
Größenvergleich ein handelsüblicher Schlüsselanhänger mit einer Breite von zwei Zentimetern.

