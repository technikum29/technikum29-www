	  <h3>WANG 700</h3>
      <div class="box center auto-bildbreite">
          <a href="/en/devices/wang700.php" title="Detailed Photo of WANG 700"><img src="/shared/photos/rechnertechnik/wang700-anlage.jpg" alt="WANG 700" width="600" height="438" /></a>
