Vortragsankündigung: Wir werden den aktuellen Stand der Neuorganisation des Museums
am 12. und 13. Oktober 2019 im Deutschen Technikmuseum in Berlin vorstellen, und
zwar auf dem <a href="https://www.vcfb.de/2019/">Vintage Computing Festival
Berlin 2019 (VCFB)</a>. Geplant ist ein 45-Minütiger Vortrag, siehe 
<a href="https://www.vcfb.de/2019/vortraege_workshops.html">Ankündigung</a>
auf der Konferenzwebsite:

<blockquote style="margin: 1.5em 0 1.5em 2em">
<strong>technikum29: Zukunftspläne eines privaten Computermuseums</strong>

<p>Das technikum29 ist ein privates Technikmuseum im Rhein-Main-Gebiet mit Schwerpunkt auf der Computergeschichte zwischen den 1920er- und 1980er-Jahren. Diese Zeitspanne umfasst den Übergang zwischen mechanischen Rechenmaschinen, über Lochkarten-EDV, bis hin zu Rechnern mit integrierten Schaltkreisen (ICs). Darüber hinaus umfasst die Sammlung einige andere Themen, etwa die Entwicklung von Radios und Fernsehern dieser Zeit. Nahezu alle Exponate auf den 250 Quadratmetern der Ausstellung sind funktionsfähig und werden während der Führungen vorgeführt. Im Jahr 2018 verstarb Heribert Müller, der Gründer des Museums. Zur Zeit versuchen seine Erben zusammen mit einem kleinen Kreis Technikbegeisterter, das Museum mit neuem Konzept fortzuführen. Im Vortrag möchten wir die Sammlung vorstellen und über die Zukunftspläne des technikum29 berichten.

<p>Sprecher: Roland Langfeld und Sven Köppel
</blockquote>

<p>Die Vorträge werden gestreamt und später auf
