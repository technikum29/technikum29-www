	<h2>Der 5-Bit-Fernschreibcode</h2>

	<div class="box center auto-bildbreite">
        <img src="/shared/photos/kommunikationstechnik/telegrafenalphabet.jpg" width="414" height="391" alt="Fernschreiber" />
		<p class="bildtext">
            <b id="decoder">5-Bit-Fernschreibcode </b> <a class="popup" href="/shared/photos/kommunikationstechnik/telegrafenalphabet.jpg"> Bild vergrößern</a>
        </p>
	</div>
    <p>Diesen sehr großen (70cm x 60cm) und schönen "Decoder", der den 5-Bit-Code in das internationale Telegrafenalphabet übersetzt, haben wir vor der Entsorgung an einer
    Technischen Universität gerettet.<br>
    Er ist von den dortigen Werkstätten ca. in der Zeit zwischen 1937 und 1949 als aufwändiges Demonstrationsgerät für Vorlesungen und Praktika gebaut worden.<br>
    Man stellt eine beliebige 5-Bit-Zahl an den groß dimensionierten Schaltern ein, dreht die Codierscheibe und erhält an einer bestimmten Stelle das dazugehörige, oben aufleuchtende
    Zeichen. Hier würde z.B. bei der Einstellung 10101 der Buchstabe "Y" leuchten.<br>
    Was damals fortschrittliche Nachrichtentechnik war, dient heute im technikum29 als sogenannte "Dechiffriermaschine". In unserem <a href="/de/lehrerinfo.php#experimente">
    Experimental-Workshop</a> können Kids und Jugendliche damit Binärtexte entschlüsseln und weiter verarbeiten.</p>
