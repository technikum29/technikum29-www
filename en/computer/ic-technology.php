      <p>The picture shows a unique system with a <b>WANG 700</b> at its heart. It is a one-of-a-kind machine and was built specifically for the department of mathematical didactics of a German university. Students of pure mathematics were forced to compete for the scarce CPU time on the university's mainframe while students of mathematical didactics had exclusive access to the system shown which eventually led to some arguments between both faculties.</p>
      <p>On the left the calculator itself can be seen &ndash; it features a two line display made from so called NIXIE-tubes, has 2 kB core memory and a built in cassette drive for mass storage. For years this machine was the fastest desktop calculator in the world.</p>
      <p>In the middle the optical reader can be seen (which, in fact, obscures the manual punch card reader which can not be seen in this picture). On the right is a modified IBM selectric typewriter which can not only be used to print out text, but to create plots of functions as well. Below the calculator an alphanumeric keyboard can be found; on the bottom an additional dual cassette tape drive is located. Programming of this machine was quite cumbersome - a simple plotterprogram took 794 program steps to perform a task which could be done on a later HP desktop calculator like the HP 9830 in only 36 steps using HP-BASIC as its programming language.</p>
