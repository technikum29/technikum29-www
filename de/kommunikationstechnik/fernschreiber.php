		 <p>Der ratternde Fernschreiber bestimmte Jahrzehnte das Bild der schnellen Nachrichtenübermittlung. 1930 erstmals von Siemens & Halske vorgestellt, wurde er am 16. Oktober 1933 von der Deutschen Reichspost eingeführt. Die erste amtliche Verbindung bestand zwischen Berlin und Hamburg. Für das Fernschreib- Telexnetz mussten separate Leitungen verlegt werden. <br/>
	    Von 21 Teilnehmern 1933 stieg die Zahl auf 1500 im Jahre 1939 und schließlich auf über 90.000 um 1975. Der mechanische Fernschreiber wurde nun auf elektronische umgestellt (Telex). <br>

Die Blütenzeit der Mechanik in der Nachrichtentechnik bestand u.a. in den verschiedenen Fernschreibertypen. Der relativ seltene Springschreiber T32 wurde ab den 30er Jahren bis in die 50er hergestellt. Es ist ein Streifenschreiber mit überschaubarem Aufwand aber ungeheuer stabil aufgebaut. Im Buch von F. Schiweck "Fernschreibtechnik" (1942) wird hervorgehoben:</p>

		<blockquote>"Der Fernschreiber ist heute eins der hervorragendsten Erzeugnisse der Feinwerktechnik, indem er in sich höchste Kunst der Getriebetechnik, Werkstoffbehandlung und Herstellung vereinigt. Seine Betriebssicherheit ist nach dem Urteil der Praxis außerordentlich groß".</blockquote>
		
		<p>In diesem voll mechanisiertem Zeitalter benötigten die "Büromaschinenmechaniker" ein gefühlvolles Händchen, gute Ohren um eventuelle Störgeräusche einordnen zu können und jede Menge Ölkännchen mit verschiedenen Ölsorten. In den 80er Jahren ist dieser Beruf schließlich ausgestorben.</p>
		
	
	
   
		
