<p>Wie wir <a href="/blog/2019-09-20-Ankuendigung-Kickoff.php">ankündigten</a>, haben
<a href="/de/team.php#roland">Roland</a> und <a href="/de/team.php#sven">Sven</a> am
vergangenen Sonntag, dem 27.10.2019 in den Räumen des technikums in Kelkheim-Hornau
zu einem Auftakttreffen (oder "Kickoff-Treffen") eingeladen. Über die Einladung selber
<a href="/auftakt/Kelkheimer-Zeitung.pdf">berichtete auch die Lokalpresse</a>.

<h3>Großes Interesse an der Museumszukunft</h3>
<p>Unsere Teilnehmerliste füllten wir anfangs, in dem wir unsere Liste an
Interessenten der Museumszukunft, die wir seit dem Tod Heriberts führten, sowie
Interessenten an einer Führung, sowie Kulturschaffende und Technikexperten aus
dem Rhein-Main-Gebiet einladeten. Aus unseren 33 Anmeldungen wurden zu unserer großen
Überraschung fast 50 Teilnehmer, sodass weder unsere Bestuhlung noch Verpflegung
ausreichend war: So voll war das Museum noch nie!

