   <h3 id="t555">TEKTRONIX Oszilloscopes</h3>
	<div class="box left clear-after">
        <img src="/shared/photos/kommunikationstechnik/tektronix555.jpg" alt="Tektronix 555" width="396" height="448" />
		<p class="bildtext">
		In the heyday of pulse technology as used in radar and computing the development of high performance oscilloscopes was necessary. This is where an Oregan based company named Tektronix Inc. made a name for themselves. Their products are examples of high precision measurement equipment showing great workmanship and extraordinary performance. In addition to that Tektronix oscilloscopes
came with service manuals that are a joy to read and a source of knowledge in electronic circuit design. Over the years Tektronix oscilloscopes became the epitome of high quality commercial oscilloscopes nearly unrivaled by other companies.<br>
On the left hand side the truly gigantic Tektronix Type 555 oscilloscope is shown. The oscilloscope sitting next to it is a Type 564 which is also quite big but does not compare with the 555. 
The Type 555 was first sold in 1963 and is a true dual-beam oscilloscope with two individual time bases. This makes it superior compared with traditional dual-channel oscilloscopes with only a single time base and an electronic chopper for the dual-channel display. <br>
The 555 features a separate power supply which is sitting on the bottom of the oscilloscope cart (a scope mobile). All in all the 555 contains about 100 tubes - an extraordinary amount for such a device. As a result the power requirement of the 555 is quite high with about 1 kW.<br>
The maximum signal frequency that can be displayed with the 555 is 33 MHz - an outstanding value for the time. Oscilloscopes like the 555 were normally carried from one place to another on a cart (it is next to impossible for a single person to lift such a device). Therefore the scope mobile often had storage space for additional modules that were not regularly used.<br>
The 555 on display is in mint condition---a rare circumstance since most  oscilloscopes from this time show signs of their heavy duty use.<a class="popup" href="/shared/photos/kommunikationstechnik/555.jpg">  <b>A high resolution picture of the 555</b></a> can be seen here.<br>

The Type 564 oscilloscope on the scope mobile on the right uses a bistable storage tube---a technology that was not mature at this time. These tubes were characterized by a short lifetime. Most notably the brightness of the display decreased rapidly with time and the maximum beam velocity for
storing images was about 500 cm/ms.<br>
Below the 564 is a Tektronix Type 453 - a small oscilloscope which is quite difficult to service and repair due to its small physical size. By the way: Modern Tektronix oscilloscopes offer bandwidths up to 80GHz!
		</p>
