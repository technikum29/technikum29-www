<p>Das 1929 hergestellte Telefunken Gerät Type 40W ist insbesondere im Aufbau sehr ästhetisch und klar gegliedert. Technik war zu dieser Zeit auch für gebildete Laien absolut transparent und bis in die Einzelheiten nachvollziehbar. Dafür musste man im Komfort Abstriche machen: So konnte man z.B. die gewünschte Lautstärke nur durch geschickte Kombination von Antennenanpassung (links) und Rückkopplung (rechts) einstellen. War die Rückkopplung zu groß, so wurde aus dem Rundfunkempfänger schnell ein "Störsender", was sich durch lautes Pfeifen bemerkbar machte.</p>

