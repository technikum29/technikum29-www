 <p><b>12.11.2022: Es geschehen noch Wunder ! </b>Einer unserer Unterstützer fand tatsächlich 2 Cassetten
 für die MLU 600 in unseren Depots !!! Die Suche nach passenden Cassetten hat ein Ende. Nach dem Reinigen
 ein vorsichtiger Blick ins Innere der Cassetten zeigt eine solide Mechanik, die ein Endlos-Magnetband 
 an einem Tonkopf vorbei transportiert. Leider hat sich in beiden Cassetten eine Gummi-Andruckrolle 
 in eine teerartige Masse verwandelt. In einer Cassette ist das Band nur geringfügig verschmutzt, in
 der zweiten Cassette hat es wohl einen Transportversuch gegeben: das Band hat sich um die klebrige
 Rolle gewickelt, verknittert und die Magnetschicht löst sich an einigen kleinen Stellen ab.
