<p>Sehr reges Treiben herrschte im technikum29 am letzten Samstag: mehr als 15 Unterstützer des
technikum29 trafen sich im Museum, um verschiedensten Aktivitäten zum Erhalt der Sammlung
fortzuführen, zu diskutieren bzw. zu starten:

<ul>
<li>Die Reparaturarbeiten an den <a href="/de/rechnertechnik/personalcomputer.php">WANG 2200</a>-Systemen (Christian, Georg, Martin und Michael) und der <a href="/de/rechnertechnik/lgp30.php">LGP30</a> (Jürgen M) gingen weiter! Die WANGs zeigen wieder ein Lebenszeichen, die LGP30 widersetzt sich unseren Reparaturversuchen weiterhin hartnäckig.

<li>Unsere <a href="/de/rechnertechnik/ibm1130.php">IBM1130</a> druckt wieder und ist damit wieder für Vorführungen einsatzbereit (Roland, Reinhard&nbsp;F)!

<li>Zwei neue Mitstreiter (Detlef, Thorsten) kümmerten sich um unsere <a href="/de/kommunikationstechnik/faxtechnik.php#Fernschreibtechnik">Fernschreiber</a> und werden diese in Kürze mit dem <a href="https://www.i-telex.net/">weltweiten Fernschreibnetz i-Telex</a>, das Enthusiasten im Internet betreiben, verbinden. Das wird ein neues Highlight für Führungen werden.

<li>Eine <a href="/de/rechnertechnik/programmierbare.php#101">Olivetti Programma P101</a> wurde erfolgreich repariert, die Arbeiten an unserer großen <a href="/de/rechnertechnik/kommerzielle.php#olivetti-p203">Programma P201</a> gehen voran (Jürgen S).

<li>Mit unseren langjährigen UNIVAC-Unterstützern wurde die Wiederinstandsetzung des <a href="/de/rechnertechnik/univac9400.php">Univac 9400-Rechenzentrums</a> nach langer Pause besprochen (Hrn. Barnowski &amp; Schale).

<li>Zwei junge Unterstützer (David, Alexander) möchten sich um ein Inventarisierungssystem kümmern und ggf Patenschaften für einige Computer übernehmen.

<li>Reinhard M will sich um unser großes TMZ-Bandlaufwerk (IBM 1130) kümmern

<li>Unsere Infrastruktur wurde verbessert: wir haben jetzt im Museum einen Internetanschluß und einen WLAN-Hotspot (Sven, Thilo).

<li>Ralf hatte sich schon vor einiger Zeit angeboten, sich um die Haustechnik zu kümmern. Aktuell wird die Klimasituation im Museum erfasst, um Feuchteschäden durch die unregelmäßige Heizsituation zu vermeiden.

<li>Christan kümmert sich mit Sven um ein Whitepaper, um die Diskussion zur Zukunft des Museums voranzubringen.

<li>Roland hat mittlerweile ungefähr <a href="/de/termine.php">zehn Führungen</a> erfolgreich durchgeführt, unser Gästebuch füllt sich mit vielen begeisterten Kommentaren unserer Besucher.

<li>Reges Treiben herrschte auch in den Depots: Dutzende Kisten mit Ersatzteilen und viele Geräte wurden ins technikum29 transferiert (Sven und Helfer)

</ul>

<p>Wir waren überrascht von der Resonanz auf unsere Einladung und werden in Kürze möglichst
in engerer Folge weitere  Möglichkeiten zu gemeinsamen Arbeiten im Museum anbieten.
Aktuelle Termine werden auf unserer Webseite unter <a href="/de/termine.php" class="go">Termine</a> bekannt gegeben.

<p>Wir möchten an der Stelle auch auf <a href="https://twitter.com/technikum29">unseren neuen Twitter-Account @technikum29</a>
