     <p>Der Blattschreiber Lo15b gehört zur zweiten Generation von Fernschreibern der Lo15-Serie, die von der Firma Lorenz seit 1930 als Lizenzbau der Teletype
     (damals noch Kleinschmidt-Morkrum) Model 14 und 15 gebaut wurden. Unser Modell stammt von 1960 und war laut dem original erhaltenen Kennungsgeber noch bis 1983 bei der Firma
     Rudolf Hoffmann in Nürnberg im Einsatz. Die mechanischen Fernschreiber aus dieser Zeit waren bei regelmäßiger Wartung nahezu unverwüstlich und über viele Jahre im Einsatz.<br/> 
    Der grundsätzliche mechanische Aufbau des Lo15 wurde über alle Gerätegenerationen kaum verändert.<br/>
    Wie auch die Siemens Fernschreiber wurde der Lo15 in den unterschiedlichsten Gehäuseformen angeboten. Unser Holzstandgehäuse befindet sich, wie der Fernschreiber selbst,
    in einem sehr guten Zustand.<br/>
    Über das rechts neben dem Fernschreiber angeordnete Fernschaltgerät FZGT 564 war der Fernschreiber mit der TW39-Vermittlung verbunden (TW = Teilnehmerwahl). Das FZGT 564 hat einen
    Lokalmoduls, also eine lokale Linienstromversorgung, um ohne Verbindung zur Vermittlungsstelle Lochstreifen erstellen und drucken zu können.</br>
    Um Zeit und damit Geld bei der Übermittlung der Nachrichten zu sparen, besitzt der Lo15b einen integrierten 5-Kanal-Lochstreifenstanzer und -sender, um Nachrichten "offline"
    vorzustanzen und dann direkt vom Lochstreifen zusenden.</p>

