	<p>Seit Dezember 2013 sind wir im Besitz einer schönen und auch sehr interessanten "Elektronischen Rechenanlage" von IBM. Dieses Modell wurde im Dezember 1965 angekündigt und ab 1966 ausgeliefert. Die Anlage war vorwiegend für Wissenschaftler, Ingenieure und Mathematiker konzipiert. Auch für sehr viele Fachhochschulen war dieser Rechner der Einstieg in die elektronische Computerwelt. Die Mittel hierfür wurden schnell bewilligt, wenn man zeigte, dass damit nebenbei die gesamte Verwaltung der Hochschule auf EDV umgestellt werden konnte. So war unsere 1130 ursprünglich der erste größere Rechner der Hochschule Darmstadt, die im Laufe der Zeit von einer "Staatlichen Ingenieurschule" zur "UNIVERSITY OF APPLIED SCIENCE" avancierte. Auf der Extraseite wird die Anlage noch an der Hochschule stehend beschrieben: <a class="go" href="/de/geraete/ibm1130-hochschule.php" target="_blank"><b>[IBM-Anlage im RZ]</b></a>. 
		
	<br><br>
	IBM schätzte den Bedarf für Deutschland bei der Einführung der 1130 auf insgesamt ca. 100 Stück. Doch es kam anders. Da die eingebaute Wechselplatte sowie die gesamte Anlage konkurrenzlos preiswert waren wurde etwa die Hälfte der ausgelieferten Systeme quasi "zweckentfremdet" rein kommerziell genutzt. Damit konnte der Absatz in Deutschland bis 1970 auf 385 Stück gesteigert werden. Je nach Ausbaustufe kostete die Monatsmiete der Anlage zwischen ca. 3500 DM bis 14500 DM. Das entsprach dem 3- bis 12-fachen monatlichen Durchschnittsgehalt von 1970. Damit war die Rentabilität des Rechners recht hoch.

		<br><br>
	IBM hat schon ab 1964/65 mit der <b>SLT</b>-Technik (Solid Logic Technology) die <b>SMD</b>-Technik entwickelt (SMD: Surface-Mounted-Device, deutsch: Oberflächenmontiertes Bauelement). Erst Mitte der 80er Jahre hat sich diese Technik mit den vielen Vorteilen langsam durchgesetzt. Siehe auch Reparatur-Blog vom 30.9.2014.<br>
	Unsere Anlage ist mittlerweile voll funktionsfähig. 
</p>
	
