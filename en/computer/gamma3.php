		<p>
        The BULL GAMMA 3 was build since 1952. At these days, such devices were
        called "electronically calculating device" (original translation
        from the BULL maintenance manual), "electron computer" or actually
        "electron mastermind".
        <br/>The device could be connected to several punchcard devices, for
        example the <a href="tabulating-machine.php">Tabulating Machine BS</a>,
        to the card copier PRD or to the ULP puncher.<br><br>
		The former director of the BULL company, Pierre Letort, described in the magazine "Arts et Manufactures", No. 22, dated June 1953, the operation of the GAMMA 3:<br>
		
		<blockquote>"In its current configuration, the computer acts as an extension unit for the
punched card machine it is connected to. The cards are read in the reader
station which transmits data to the computer.<br> The computer in turn performs all
necessary calculations and transmits the results back to the punched card
machine which will print or punch these values. Regardless of the task, the
computer is so fast that there is no visible delay caused by the calculations".</blockquote>
      </p>
  
