<h3>WANG 320</h3>
    <p>Hochinteressant ist auch der 1967 gebaute wissenschaftliche Rechner "WANG 320". Ein extrem seltenes Ger&auml;t mit Lochkartenprogrammierung und mehreren Keyboards, f&uuml;r die damalige Zeit eine Weltsensation.</p>

    <div class="box center auto-bildbreite">
        <a href="/de/geraete/wang320.php" name="backlink-wang320"><img src="/shared/photos/rechnertechnik/wang320.jpg" alt="WANG 320 SE" width="690" height="287" /></a>
        <p><b>WANG 320 SE</b></p></div>
		
             <p>Für 1966/67 war dies ein Tischrechner der Superlative. Zwei Eigenheiten waren damals sensationell: Die sehr schnelle Ermittlung von Werten zur Exponential- und Logarithmusfunktion (schneller als die Berechnung der Wurzel einer Zahl) sowie die Tatsache, dass bis zu 4 Keyboards angeschlossen werden konnten. Alle Anwender können gleichzeitig auf den Rechner zugreifen. Längere Programme die man per Lochkarte eingibt, werden unterbrochen um kurze Berechnungen von einem anderen Keyboard zu übernehmen. Danach wird das Programm weiter ausgeführt. Ein ideales Gerät für Schulen und Universitäten. Die präparierten 80-spaltigen Lochkarten können offline mit einem Stift auf einer Schablone "gestanzt" werden. Während an den meisten Hochschulen noch mit dem Rechenschieber gerechnet wurde, begann bei den wenigen, die im Besitz dieser Rechner waren bereits die Zeit der Informatik. Durch Anklicken des Bildes erhalten Sie eine Detailaufnahme.
        </p>
    
<h3>Hewlett Packard HP 9100</h3>

    <div class="box center auto-bildbreite">
        <img src="/shared/photos/rechnertechnik/hp-9100.jpg" alt="HP-9100" width="557" height="432" />
        <p>
		    <b>HP 9100</b></p></div>
			<p>1968 brachte der erste Tischrechner von HP die Wissenschaftler zum staunen. Er ist ohne ICs aufgebaut und beherrscht alle wesentlichen wissenschaftlichen Funktionen. Als interner Speicher dient ein Kernspeicher, bei dem auch nach dem Abschalten Programme und Daten nicht verloren gehen. Als externer Speicher werden Magnetkarten (kleiner als die von Olivetti) verwendet. Der aufgesetzte Drucker war optional. Als Anzeigemedium dient eine Oszilloskopröhre, mit welcher der Inhalt von 3 Registern abgebildet wird. Die Zeichengenerierung wird von einem gefädelten ROM übernommen. Mit diesem relativ kompakten Rechner hat HP Maßstäbe gesetzt. Billig war ein solcher Rechner nicht: Er kostete anfangs 23.000,- DM  (etwa 12.000 Euro) und war daher für Privatpersonen praktisch unerschwinglich. <br/>
            Der Hauptkonkurrent WANG konnte erst 2 Jahre später mit dem WANG 700 (s.u.) nachziehen.</p>
        </p>

	
