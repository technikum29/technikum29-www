	<p>When school classes come visiting our museum, we typically offer one of the two tours in the two main
	museum topics which we call <b>Computer history</b> or <b>The rise of communication technology</b>. We never
	offer both courses after each other for a single class, purely as a matter of time and concentration for
	the audience. Instead, we decide in advice with the particular teachers. 
	
	<p>We offer courses in two intensity levels. Either classes can participate in only a single tour (ca.
	90 minutes). Instead, they can also extend their visit up to three hours and participate in an <em>experimental
	workshop</em>. This allows pupils to actually work with the historic devices, solve problems and riddles.
	
	<p>In our experience, these kind of school classes are mostly interested:
	
	<ul>
		<li>Extension courses in physics, math, engineering or adjacent topics at high/secondary level school (German gymnasiale Oberstufe, the last school form before Abitur).
		<li>Computer science classes of all age
		<li>Working groups, students in project weeks and similar of all age (voluntary courses for pupils offered at schools)
		<li>Groups from vocational schools or younger students from universities
		<li>Pupils from primary schools
	</ul>
	
	<p>For all of these groups we prepared dedicated tours and have very good feedback.
	

