<a href="http://www.technikum29.de/auftakt" rel="nofollow">www.technikum29.de/auftakt</a>
anschauen.

<h3>Administratives und Strategisches in Kurzform</h3>
<p>
Das Einzigartigkeits-Attribut der Computersammlung von Heribert Müller ist, dass „alle
Exponate funktionsfähig sind”, so war auf der Website des Museums bislang zu lesen.
Eine Bestandsaufnahme zeigte zwar, dass auch von den Highlights einiges defekt ist und
der Reparatur bedarf. Dennoch gibt dieses Attribut der Ausstellung eine deutschlandweite
Einmaligkeit, das am ehesten etwa mit dem „Living Computer”-Museum in Seattle vergleichbar ist.

<p>
Wie schon oft mit einigen von euch besprochen, ist es den drei Kindern (Erben) unmöglich, die
von Heribert im Alleingang instand gehaltene/unterhaltene Sammlung zu betreiben. Ein Fortbestand
„auf den Schultern von vielen” ist jedoch einen Versuch wert, undgenau dafür möchten wir nun den
Grundstein legen.

<p>
Hierfür existieren viele Ideen, und eure sind willkommen: Etwa die Schaffung eines „Hackerspace“-Umfeldes
oder Übernahme von Patenschaften für einzelne Geräte.Sicherlich muss das Museum auch
sichtbarer werden im Rhein-Main-Gebiet und darüberhinaus. Einer inhaltlichen Refokussierung
steht nichts im Wege, auch die umfangreichenDepots können genutzt werden (mit bereits über 800
inventarisierten Geräten).

<p>
Die Rettung des privaten Technikmuseums ist die Eigentumsimmobilie Am Flachsland 29 in Kelkheim,
in der es auf 250m² untergebracht ist. Ziel ist die Gründung einer gemeinnüt-zigen Organisation,
wobei hier der eingetragene Verein am sinnvollsten erscheint. Diese Organisation soll Eigentümer
der Techniksammlung werden, die sich derzeit noch in denHänden der Erben von Heribert Müller
befindet. Außerdem soll sie Mieter in der Immobiliewerden, um mit einer minimalen Miete die
Rentabilität des Gebäudes sicherzustellen.

<p>
Für einen Verein brauchen wir mindestens sieben Gründungsmitglieder. Ein finanzieller Rahmen
soll durch den Verkauf von Geräten entstehen. Zur Deckung grundlegender Unkosten (etwa
Materialien und Miete) ist er auch auf Spenden und Mitgliedsbeiträgeangewiesen.

<h3>Eine Agenda für das Treffen</h3>

<p>Wir treffen uns im Musem, wo uns Kaffee, Sitzplätze und natürlich auch die Ausstellung
selber begrüßen. Bislang planen wir:

<ul>
  <li>Eine Vorstellungsrunde „Who is Who“, [15min]
  <li>Ein geführter Gang durch die lauffähigen Exponate, [50min]
  <li>eine kurze Präsentation über den Status Quo bzw. unsere Vorarbeiten: Laufende
      Restaurationsprojekte, Aufbau eines Arbeitsplatzes, Überblick über die Depots,
      Inventarisierung, Website, Blog,  [20min]
  <li>Termin für die Gründung eines Vereins [10min]
</ul>

<p>
Wir freuen uns über Vorschläge, welche Punkte auf dieser Tagesordnung fehlen. Insgesamt rechnen
wir damit, dass wir innerhalb von zwei Stunden fertig sind. Wir können unsauch ein „Open End“
vorstellen (bei gutem Wetter z.B. im Garten).

<p>
Bitte bestätigt eure Teilnahme mit einer kurzen Mail an kontakt@technikum29.de, damit
wir eine Vorstellung bekommen, wie viele Teilnehmer uns erwarten.

