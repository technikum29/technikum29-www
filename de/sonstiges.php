        <img src="/shared/photos/start/leander.jpg" alt="Leander Schwarzer" width="313" height="239" class="nomargin-bottom" />
		<p class="Bildtext small">Leander Schwarzer im <b>technikum29</b> beim Stanzen von Lochkarten an einer IBM Maschine</p>
