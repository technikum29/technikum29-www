<p>Viele unserer Besucher fragen, ob wir im technikum29 denn auch "eine <b>Enigma</b>" hätten. Bisher war die Antwort "nein". Die Geschichte der Enigma 
	fasziniert viele Technikinteressierte, wir möchten hier nicht allzusehr ins Detail gehen, erschöpfende Informationen zu den vielen Enigma-Varianten 
	findet man an vielen Stellen im Internet, u.a. <a href="https://cryptomuseum.com/crypto/enigma/d/index.htm">im Cryptomuseum</a>. </p>

<p>Die Firma <b>Chiffriermaschinen Aktiengesellschaft</b> produzierte ab 1923 eine Reihe von Chriffriermaschinen zur elektro-mechanischen Verschlüsselung von Nachrichten. Zunächst für den 
	kommerziellen Einsatz gedacht, fand sie breite Verwendung durch das deutsche Militär im 2. Weltkrieg. Den Alliierten gelang ab 1940 die Entschlüsselung 
	der Enigma, vor allem basierend auf den Arbeiten von <b>Marian Rejewski</b> und <b>Alan Turing</b>. Letzterer entwarf bahnbrechende Konzepte und Grundlagen für Computer, 
	wie wir sie heute kennen. In Bletchley Park (GB) wurden ab 1939 basierend auf seinen Arbeiten elektro-mechanische Maschinen konstruiert, die die 
	Entschlüsselung automatisierten und die schon Grundzüge späterer Universalcomputer zeigten. Von daher gehört eine Enigma in jede Computersammlung.</p>

<p>Original Enigmas sind selten und nur wenige Einzelstücke werden zu hohen Preisen gehandelt. Technikum29 erhielt 2024 aus dem Nachlass eines begnadeten 
	Bastlers einen funktionstüchtigen <b>Nachbau (Replika) einer Enigma Typ "I"</b>, wie sie vom deutschen Militär genutzt wurde. An ihm kann während einer 
	Führung das Ver- und Entschlüsseln von Nachrichten vorgeführt werden.</p>

