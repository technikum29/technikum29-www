		<h3>Olympia RAE 4/30-3 und Wanderer Conti</h3>
		
		<div class="box center auto-bildbreite">
        <img src="/shared/photos/rechnertechnik/olympia-wanderer.jpg" alt="Olympia RAE 30 und Wanderer Conti" width="694" height="278" />
        <p class="bildtext"><b>Olympia RAE 4/30-3 und Wanderer Conti</b></p></div>
    <p>Die <b>Olympia-Werke AG</b> (Deutschland) brachte ab 1965 den "Elektronischen Vierspezies-Rechenautomat" auf den Markt. Die Besonderheit waren die Fließkomma-Automatik, 3 Rechenwerke, 2 Speicherwerke und im abgebildeten Gerät RAE 4/30-3 ein "Memoriawerk" (d.h. 3 frei zur Verfügung stehende Register). Dennoch ein durchaus solider Rechner mit handgefädeltem Kernspeicher (384 Bit: 6 Speicher * 16 Stellen mit Vorzeichen * 4 Bit !), Germanium-Transistoren und Nixieröhren aber leider ohne Schnittstelle. Damit waren weder eine Programmabspeicherung noch eine externe Programmeingabe möglich. Diesen Schritt hat Olympia verpasst, so dass der Rechner schnell veraltet war. Das Design ist mit dem schmucklosen Kunststoff-Gehäuse auch sehr dürftig. In der Sonne vergilbte das Gehäuse schnell und machte keinen guten Eindruck.<br>
	Das Gerät wurde auch in den USA von der Rechenmaschinen-Fabrik <b>Monroe</b> unter dem Typ 770 vertrieben. Es war schon beachtlich, dass eine amerikanische Firma elektronische Rechner aus Deutschland bezog.<br>
	<p>Wir haben in unserer Sammlung einige Geräte: RAE 4/15-2, RAE 4/30-1, -2 und -3, die wir nach und nach reparieren werden. </p>
	<p>Über den Fortschritt in der Reparatur unserer Olympia RAE-Tischrechner werden wir im <a class="go" href="/de/geraete/Olympia_rae/RAE_4-details.php">Olympia RAE-Reparaturblog</a> berichten.</p>
	Auch die legendäre <b>WANDERER-WERKE AG</b>, wie Olympia eine typische Büromaschinenfabrik, sah den Zug der Zeit abfahren und brachte daher ebenfalls 1965 einen Rechner auf den Markt. Werbewirksam formulierte WANDERER: "Der Welt erster druckender elektronischer universal Tischrechenautomat". Dieser Marktvorteil war Wanderer jedoch nur für einige Wochen gegönnt; die Zeit begann hektisch zu werden, Olivetti und Diehl zogen nach.<br>
	Lesen Sie dazu den <a class="go" name="backlink-wanderer" href="/de/geraete/wanderer.php">Originalprospekt</a>.</p><br>
	
