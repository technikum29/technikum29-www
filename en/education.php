<h3 id="electronic">Electronic workshops for youths</h3>
<p>We also offer the complete opposite to museum tours. Instead of getting pupils in touch with
the historical device, we offer completely different workshops on modern technology well inside
the museum. That is, we currently offer two workshops:

<ul>
<li>The <a href="/en/robotics.php">LEGO robotics workshop</a>. It's especially dedicated
    to the youngest of our visitors and enables them to build and program robots made by
    the LEGO Mindstorms company.
<li>The <a href="/en/physical-computing.php">Physical computing workshop</a>, alias
    <em>The Arduino lab</em>: This is an advanced course for youths to teach them basic
    electronics, microcontroller programming and all that with a connection to sensors
    and all that.
</ul>

<h3 id="birthday-events">Birthday events for kids and youths</h3>
<p>As we do so many with children, this is even another kind of events we frequently host:
Children can celebrate their birthday with all their friends at our location. First, they
participate a short guided tour which is especially appropriate to their age. Afer a short
break, the kids experiment with so interesting experiments that it's always hard to find an
end for the party. This is so different to a typical birthday party.

