	<h3 id="cpt8800">CPT 8800</h3>
	
		<div class="box center auto-bildbreite">
			<!--<a href="" title="Zur Detailaufnahme der CPT 8800">-->
			<img src="/shared/photos/rechnertechnik/CPT8800.jpg" alt="CPT 8800" width="640" height="387" /></a>
