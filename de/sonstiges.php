	<p>Diese Karten wurden im Abstand von ca. 1-2cm vor eine Wand gehängt. Die Sonnenstrahlen warfen Schatten des ausgestanzten und codierten Textes unabänderlich auf die Wand. Das Werk wurde mit einem Preis beim 32. Österreichischem Grafikwettbewerb ausgezeichnet (Innsbruck, 2011).<br>
	Nun war Herr Schwarzer motiviert, größere Lochkartenwerke zu gestalten. Dazu hat er tagelang im technikum29 Lochkarten mit Texten aus "Das Kapital" (Karl Marx) gestanzt. Es kamen ein Stapel von mehreren hundert gestanzte Karten zusammen. Damit hat Herr Schwarzer eine viel beachtete Ausstellung "Ein Symbol der Freiheit" in Piacenza (Italien) bestritten. Lochkarten verwandeln hier zeitgenössige Slogans in visuelle Paradoxien, wenn sie in größeren Mengen an der Wand hängen und ihre Schatten werfen.<br>
	<div class="box desc-right borderless">
        <img src="/shared/photos/start/lk-musik.jpg" alt="Musik aus Lochkarten" width="336" height="188" class="nomargin-bottom" />
		<p class="Bildtext small center">Lochkarten durchlaufen eine "Spieluhr"</p>
	</div>
	
	<p>In einem anderen Werk: "Fetischcharakter der Ware" werden die Texte von Karl Marx in den gestanzten Lochkarten aneinandergehängt und unter einer Maschine durchgezogen die, ähnlich einer mechanischen Spieluhr, gestanzte Löcher in Töne verwandelt. So entsteht "Das Kapital"  als verfremdete Musik in Spährenklängen.<br>
	Wir werden dieses Projekt aufgreifen und mit Hilfe optischer Lochkartenleser über einen Microcontroller quasi einen Synthesizer steuern: Die technische Perfektion von Kunst mit historischen Medien.</p>
	
	
	
