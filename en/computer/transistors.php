	<h3>FRIDEN 130 (132)</h3>
    <div class="box center auto-bildbreite">
        <img src="/shared/photos/rechnertechnik/friden130.jpg" alt="Friden 130" width="694" height="497" />
        <p class="bildtext center"><b>FRIEDEN 130</b></p>
	</div>
	<p>
	    The American <b>Friden Calculating Machine Company</b> was already a pioneer in desk
		calculator technology: in the mid-50s they built the first mechanical calculator in series
		that was able to extract a root.
	</p>
	<div class="box left clear-after">
        <img src="/shared/photos/rechnertechnik/friden-display.jpg" alt="Display des Friden 130" width="274" height="134" />
		<p class="bildtext">
		    The <b>FRIDEN 130</b> was announced in 1964. It was the first desktop calculator featuring a
		    CRT display, using an oscillocope tube to display the contents of four internal registers of the machine.
	        The memory is based on a <a class="go" href="storage-media.php#Magnetostrictive_memory">magnetostrictive line</a>.
            <br />The overall design of the calculator is quite futuristic - the machine might well be found in
		    a space travel movie of that time. The smallest model featuring only the four basic arithmetic
		    operations was sold for about 5000 DM while the larger model, the FRIDEN 132, which included a
		    square root function, was priced at 6700 DM.</p>
			A beautiful advertising leaflet, one finds here:	<a class="go" href="http://archive.computerhistory.org/resources/text/Friden/Friden.130.1964.102646292.pdf" target="_blank">"Mathematics for the Space Age"</a> 
		</p>
    </div>

