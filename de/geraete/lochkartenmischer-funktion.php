<h2><?php print $title; ?></h2>

<p>Was macht nun ein Lochkartenmischer?  Dazu muss man wissen, dass Betriebsvorgänge in der Lochkarten EDV regelmäßig in feste (z.B. Adressen) und veränderliche (z.B. Umsätze) Teile zerlegt und auf getrennte Lochkarten übernommen wurden. Das erscheint ganz sinnvoll, da Adressen wenig Änderungen unterliegen während sich Umsätze fortwährend ändern.</p>

<p>Wenn man z.B. auf einer Tabelliermaschine Rechnungen ausdrucken lassen wollte, war vorher eine Gruppierung der Lochkarten entsprechend ihrer Zusammengehörigkeit erforderlich.</p>

