<h2>Transparenz: Struktur und Finanzierung des Museums</h2>
<p>Auf dieser Seite legen wir die Struktur und Finanzierung des technikum29-Computermuseums
offen. Es handelt sich um eine Auflistung im Sinne der
<a href="https://www.transparency.de/mitmachen/initiative-transparente-zivilgesellschaft/">Initiative transparente Zivilgesellschaft</a>. Wir hoffen
durch die Transparenz <a href="/de/unterstuetzen.php">UnterstützerInnen</a> zu finden.
  
  
<h3>1. Name, Sitz, Anschrift und Gründungsjahr</h3>
<p>Wir sind das <strong>technikum Computermuseum</strong> (eigentlich <em>technikum29</em>),
mit Sitz in Kelkheim/Taunus, die genaue Anschrift steht in unserem <a href="/de/impressum.php">Impressum</a>.
Das Museum wurde unter diesem Namen 2005 privat gegründet.

<h3>2. Vollständige Satzung sowie Angaben zu den Organisationszielen</h3>
<p>Derzeit sind wir immernoch ein <em>privat</em> geführtes Museum. Es gibt aber bereits einen Satzungsentwurf
für einen gemeinnützigen Verein, der gegründet werden soll; außerdem Kandidaten für die Vorstandspositionen.
In diesem Sinn ist das Museum möglicherweise im Zustand eines <em>Vorvereins</em>.

<p>Das Organisationziel liest sich in der Präambel so:

<blockquote>
Der technikum-Verein hat das Ziel, den Stand der Rechen- und Computertechnik des letzten Jahrhunderts
mit Schwerpunkt von den 30er bis 70er Jahren (Sammlungsgebiet) durch Erhalt und Pflege von Exponaten
der Technikgeschichte zu dokumentieren und ein breites Publikum durch Vorführungen und Vorträge mit
den Wurzeln unserer heutigen digitalen Welt bekannt zu machen.
<p>
Kernaufgaben des Vereins ist der Betrieb eines Museums, darunter fallen u.a. die Inventarisierung, die
Reparatur und Instandhaltung der Exponate, die Präsentation der Sammlung durch Vorführungen und
Veröffentlichungen sowie die Unterstützung von Kunst und Kultur auf dem Sammlungsgebiet in Deutschland
u.a. durch Vorträge und zeitweiliger Präsentation anderer Sammlungen (Wanderausstellungen).
<p>
Der Verein wird hierzu in angemieteten Räumlichkeiten sowohl Leihgaben als auch eigene Exponate
beherbergen. Die Sammlung des 2018 verstorbenen Gründers des Privatmuseums „technikum29“ wird von
dessen Erben als Dauerleihgabe in den Verein übertragen.
</blockquote>

<p>Der Vereinszweck wird wie folgt definiert:

<blockquote>
Zweck des Vereins ist die Förderung von Kunst, Kultur, Wissenschaft und Forschung auf dem o.g.
Sammlungsgebiet im Rahmen der in der Präambel abgesteckten Zielsetzung.

<p>Der Vereinszweck wird insbesondere verwirklicht durch
<ul>
<li>Dokumentation der Sammlung durch Inventarisierung.
<li>Erweiterung der Sammlung durch Anwerbung von Leihgaben und Ankauf.
<li>Präsentation der Sammlung durch Vorführungen und Veröffentlichungen.
<li>Öffentlichkeitsarbeit auf dem Sammlungsgebiet, z.B. durch eigene und eingeladene Vorträge
und Präsentation von Sammlungen Dritter (Wanderausstellungen).
<li>Unterstützung der Technikwissenschaften auf dem Sammlungsgebiet, z.B. durch Verleih von
Exponaten, Austausch von Dokumenten und wissenschaftliche Veröffentlichungen.
</ul>
</blockquote>

<h3>3. Angaben zur Steuerbegünstigung</h3>
<p>Das Museum ist derzeit in Privathand. Deswegen können wir <strong>keine</strong>
steuerbegünstigten Spendenquittungen ausstellen.

Die Museumsräume genießen gemäß Beschluss des hessischen Ministeriums für Wissenschaft und Kunst
von 2005 eine Grundsteuerreduktion (ca. 100 EUR weniger im Quartal).

