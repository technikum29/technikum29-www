<p>Liebe Freunde und Unterstützer des technikum29,

<p>
ein herausforderndes Jahr neigt sich dem Ende zu und wir möchten es nicht versäumen, allen Unterstützern und Helfern für ihre Arbeit und Unterstützung in 2020 zu danken. Wir wünschen euch, euren Familien und Lieben gesunde, ruhige Festtage und ein erfolgreiches Neues Jahr!

<h3>Was geschah 2020?</h3>

<h4>Führungen</h4>
<p>Das Jahr 2020 war geprägt durch Covid-19 und den damit einhergehenden Einschränkungen. Zu Beginn des Jahres konnten wir noch über <strong>10 Führungen</strong> mit über 150 Teilnehmern absolvieren. Unsere Gäste waren durchweg begeistert und füllten das Gästebuch mit Kommentaren und unser Sparschwein mit dem einen oder anderen Schein. Aber im März zeichnete sich ab, dass wir keine Führungen zu Corona-Bedingungen anbieten können: das Abstandsgebot und stundenlanges Maskentragen lassen sich nicht durchhalten. Von daher ruht der Führungsbetrieb seit März bis auf weiteres auch ins Jahr 2021 hinein - leider !

<h4>Organisatorisches</h4>
<p>Eine mögliche <strong>Vereinsgründung</strong> ist zunächst aufgeschoben, einerseits wegen der Beschränkungen durch Corona, andererseit wg. Diskussionen um die geeignete rechtliche Form für das Museum. Technikum29 ist also nach wie vor eine private Sammlung, was dem Schwung der Helfer und auch der einen oder anderen Spende keinen Abbruch getan hat.

<p>
Trotz aller Beschränkungen konnten wir doch einige Erfolge erzielen: wir konnten Anfang des Jahres noch 2 Helfertage abhalten, bei dem bis zu 20 Helfer Maschinen reparierten. Nach den Corona-Beschränkungen kamen einzelne Helfer ins Museum, und mit großem Abstand wurde das eine oder andere Gerät weiter instand gesetzt bzw. erstmalig wieder in Betrieb genommen. <strong>Wir danken den betreffenden Helfern hier summarisch</strong>, um in der Einzelaufzählung niemenden zu vergessen.

<h4>Depots</h4>
<p>
In den <strong>Depots</strong> wurde kontinuierlich aufgeräumt, hin- und hertransportiert und
Platz geschaffen. Aber es ist eine Sisyphusarbeit!
Die Fortschritte sind noch gering, und Überschwemmungen im Folge eines Gewitters
im Sommer haben die Lage nicht verbessert. <a href="/blog/2020-09-03-Lagersuche.php">Wir suchen dringend bezahlbaren
Lagerraum auf Zeit, um das Sortieren der vielen Geräte(fragmente) voranzutreiben.</a>
Wer immer etwas weiss - bitte melden!

