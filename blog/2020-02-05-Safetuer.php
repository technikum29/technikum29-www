<p>
Ein Kuriosum unseres Museumsgebäudes ist, dass es sich um eine ehemalige Filiale der
<a href="http://naspa.de">Nassauischen Sparkasse</a> handelt. Dazu gehört natürlich auch
ein waschechter Banktresor, in dem früher wohl Schränke voller Geld dicht an dicht standen
(heute beherbergt er lediglich unsere <a href="/de/kommunikationstechnik/fernsehen.php">Fernseh-</a>
und <a href="/de/kommunikationstechnik/tontechnik.php">Tontechnik</a>).
Es handelt sich um einen 30 Quadratmeter großen Kleintresorraum, der ringsherum mit
60 Zentimeter dickem Stahlbeton umschlossen ist und nur durch eine tonnenschwere
Panzer-Tresortür betreten werden kann. Für die 1971 gelieferte Tresortür aus dem Hause
<a href="https://www.garny.de/">Franz Garny</a> (Frankfurt/Niederrad) liegt uns sogar ein
historisches Werksattest vor.

