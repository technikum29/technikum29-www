	    Dieser Rechner wurde ab 1952 gebaut. Man bezeichnete damals solche Rechner auch als "elektronisches Rechengerät" (original BULL-Übersetzung im technischen Handbuch) oder "Elektronenrechner" bzw. in der damaligen Hochachtung vor solchen Leistungen auch als "Elektronengehirn".
	    <br/>Der Rechner mit einem unscheinbaren Äußerem war an Lochkartenmaschinen anschließbar, so z.B. an die Tabelliermaschine BS, den Kartendoppler PRD oder den Stanzer ULP.
		<br/>Der damalige Direktor des Unternehmens "BULL", Pierre Letort beschrieb in der Zeitschrift "Arts et Manufactures", Nr. 22 vom Juni 1953, die Arbeitsweise des Gamma 3:
	</p>
	
	<blockquote> <!-- blockquote = Blockzitat, homepageweit eindeutige Formatierung -->
	    "ln seiner augenblicklichen Form arbeitet der Elektronenrechner als Zusatzgerät zu den Lochkartenmaschinen, an die er angeschlossen wird. Die Lochkartenmaschine fühlt die im Zuführmagazin eingelegten Karten ab und sendet die abgefühlten Werte an den Elektronenrechner, von dem sie umgekehrt die Ergebnisse erhält, die sie je nach ihrer Arbeitsweise schreibt oder locht. Aber welche Rechenaufgabe auch gestellt sein mag, das Ergebnis des Elektronenrechners entsteht so schnell, daß es zeitlos zu sein scheint."
	</blockquote>

	<p>
	    Unsere GAMMA 3 ist voll ausgebaut und verfügt über 7 Laufzeitspeicher mit einer Kapazität von je 12 Dezimalstellen (siehe <a href="/de/rechnertechnik/speichermedien.php#bull"> Speichermedien</a>) die sich in einen Rechenspeicher und 6 Zahlenspeicher aufteilen. Zur Speichererweiterung gab es noch so genannte "Speicherschränke" mit 24 Speichereinheiten für je 12 Dezimalstellen, also der Platzbedarf eines Kleiderschrankes zum Abspeichern von 24 zwölfstelligen Dezimalzahlen!!! <br/>Insgesamt ist der Rechner mit knapp 400 Elektronenröhren bestückt. Die Anpassung des schnellen Elektronenrechners an die langsame Lochkartenmaschine erfolgt durch Thyratrons. Eine Thyratronröhre arbeitet ähnlich wie ein Thyristor (Halbleiterbauelement) und kann binäre Zustände zwischenspeichern.
	</p>
	
