<p>
Insgesamt 
13 Unterstützer des technikum29 trafen sich am 23. und 24.11.2019 in den Räumen des Museums, um diverse
Geräte instand zu setzen. Mit ihrer Hilfe gelang es, u.a. zwei WANG-Computer wieder vorführbereit zu
machen, sowie an unserer LGP30 und unserer Olivetti P203 deutliche Fortschritte bei der Restaurierung
zu machen. Ein neues Projekt, die Digitalisierung des unschätzbaren und seltenen Programmbestands unserer
LGP21, wurde gestartet. Damit werden wir einen Beitrag zum Erhalt der Programme dieser sehr interessanten
Maschine leisten.

<p>
Einige Helfer nahmen sich sogar noch „Arbeit“ mit nachhause, um in ihrer Werkstatt mit besserer Ausrüstung
weitere Reparaturen durchzuführen.

