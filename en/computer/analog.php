    <h3>EAI 180 and EAI 185 digital-anlog computer</h3>

    <p><b>EAI 180</b> from "Electronic Associates Incorporated",
       New Jersey, is a so called <b>hybrid computer</b>  (hybris [greek]: From twofold parentage), year of manufacture 1970. It contains the parts of an analoge computer and these from a digital computer. The device is equipped with IC's from the first generation (DTL-technology). The calculation circuit is plugged with cables on the front panel. The cycle time from the analoge part can be set to less than 10ms. With that parameter, an equation will be solved at least 100 times per second. So you can watch the output with a simple oscilloscope.<br><br>
    </p>

