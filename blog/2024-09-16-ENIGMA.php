<p>In einem schönen Holzgehäuse (Bild links) befindet sich der sehr detailgetreue Nachbau einer ENIGMA I (Bild mitte und rechts). 
	Entworfen und gebaut wurde sie von Gunther Tünnermann (Kelkheim). Wir erhielten dieses schöne Stück Technikgeschichte 
	von seiner Witwe als Spende an das Museum.</p>
	
<p>Chiffriermaschinen wie die ENIGMA wurden zur Ver- und Entschlüsselung von Nachrichten genutzt. Über eine Tastatur 
	wird der Text eingegeben. Über einen mehrstufigen Prozess wird der eingegebene Buchstabe mehrfach (7 - 10 mal) durch 
	alphabetische Substitution "verwürfelt" und über ein Glühlampenfeld ausgegeben. Diese mehrfache Substitution ist bei der 
	ENIGMA konstruktionsbedings symmetrisch. Das bedeutet, dass die Entschlüsselung in gleicher Weise erfolgt: man gibt den 
	chiffrierten Text Buchstaben für Buchstaben ein und liest die Nachricht am Glühlampenfeld ab.</p>
	
	
