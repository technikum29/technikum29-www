		Doch der aufwändige und komfortable Prozessor der 1130 konnte viel mehr bieten zumal das Arbeiten mit Lochstreifen wenig komfortabel ist. Daher wurde die Anlage in der Regel mit anspruchsvollerer Peripherie verkauft bzw. vermietet. Sie war jederzeit erweiterbar und konnte so mit der Institution und deren Bedarf an EDV mitwachsen.<br><br>
		
			Die Mechanik des Druckers 1132 wurde von der Tabelliermaschine IBM-407 aus den 50er Jahren abgeleitet und unter Beibehaltung der Druckqualität etwas vereinfacht. In Bild 2 zeigt ein Foto den Drucker mit allseits geöffnetem Gehäuse und entfernten Abdeckungen. So haben unsere Besucher auch während der Vorführung tiefe Einblicke in die archaische Technik.  Nach der Restauration sieht unser Drucker außen und innen wie aus dem Ei gepellt aus.<br>
			Vorne sitzen 120 Elektromagnete zum Auslösen der entsprechenden Druckstellen, alles noch in großformatiger Mechanik, gebaut für die Ewigkeit und typisch für die 50er und 60er Jahre.<br>

			Er liefert mit 80 Zeilen pro Minute zwar ein gutes Druckbild doch wurde die Kombination von Hightech-Elektronik und langsamer Mechanik wohl nur im Hinblick eines günstigen Preises gewählt. 
			Wem der Drucker zu langsam war konnte auch den IBM 1403 Schnelldrucker anschließen.<br><br>
			
			Im Bild 3 ist ein kompletter 8K x 16 Bit (=16 KB) Kernspeicherblock mitsamt der Ansteuer- und Auslese-Elektronik zu sehen. Wie die restliche Elektronik mit den SLT-Steckkarten ist auch die Speichereinheit sehr kompakt und deutlich weniger voluminös als bei allen Konkurrenten der damaligen Zeit. Hier setzte der Konzern, bekannt durch die vielen <a class="go" href="http://www.zdnet.de/88215861/ibm-sichert-sich-auch-2014-die-meisten-us-patente/" target="_blank">IBM-Patente</a>, ebenfalls Maßstäbe. Die abgebildete Streichholzschachtel dient zum Größenvergleich. Die meisten weltweit verkauften Systeme hatten nur einen Kernspeicher mit 8K x 16 Bit. Die Erweiterung auf 2 x 8K x 16 Bit (=32 KB) (linker Anbau) ist daher deutlich seltener zu finden.<br><br>
			
			
			Auch die gehören zu unserer Anlage: <b>Facit Lochstreifengeräte</b> (Bild 4).<br>
			Aufwickler Type 4015, Stanzer Type 4060, Leser Type 4001 und Stanzerelektronik Type 5104.<br>
			Es war damals üblich, dass wissenschaftliche Rechner immer zusätzlich mit Lochstreifenperipherie ausgestattet waren. Diese Geräte sind relativ unempfindlich, so dass man im Notfall darauf zurückgreifen konnte. Es war jedoch nicht möglich vom Lochstreifen aus Programme zu starten. Dafür musste eine extra Option eingebaut werden.<br>
			
			Bild 5 zeigt die an die Anlage angeschlossen Ausgabegeräte wie Plotter und Terminal.<br>
			Der <b>COMPLOT DP-1 Digital Plotter</b> ist ein noch voll transistorisierter "Hochgeschwingigkeitsplotter" der anstelle des langsameren IBM-Modells 1627 (Calcomp 565) angeschlossen wurde. Die Technik und Elektronik des Plotters ist sehr überschaubar, nicht aber der Preis: Er kostete 1971 über 18.000,- DM (ca. 9000 €), was einem Gegenwert von zwei Mittelklasseautos entsprach. In den frühen 70er Jahren konnten EDV-Firmen noch "richtig Geld verdienen".<br>
			Rechts daneben steht das <b>Tektronix 4006 Graphic-Terminal</b> mit welchem sowohl Text als auch Grafik angezeigt werden kann. Dieses Terminal kam 1975 auf den Markt. Damals waren die Preise für Halbleiterspeicher noch so hoch, dass man stattdessen eine "Speicherröhre" verwendete. Auf einem ähnlichen Prinzip arbeiteten die ersten Speicheroszilloskope.			</p>
			
