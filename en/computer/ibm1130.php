	<p>In December 2013 we got a wonderful and interesting IBM 1130 computer. This model was announced in December 1965 and was shipped beginning in 1966. It was primarily used for scientific, engineering and mathematical purposes. Many universities for applied sciences also had such machines since funding was easy once it had been demonstrated that the machine could also be used for clerical applications as well. Our machine was once used at the university of applied sciences in Darmstadt (see here for background information: <link>).<br><br>
 
Initially, IBM estimated that a market for about 100 machines of this particular model might exist. It turned out that this estimation was far to pessimistic. Since the system featured a disk drive and was very cheap, compared with comparable machines from other vendors, about 50% of the systems running at customer locations were used in clerical applications. Thus, in Germany alone 385 machines were in use in 1970. The monthly rent was between 3500 DM and 14500 DM (this was about 3 to 12 times more than the average montly earnings of a German worker in 1970) depending on the configuration of the system.<br><br>
 
Beginning in 1964/65, IBM developed so-called SLT (Solid Logic Technology) devices - precursors of todays SMD (Surface Mounted Device) components. This technology found widespread use in the 1980s - about 20 years after IBMs pioneering developments. (Cf. our repair blog: <link>) Our machine is as a card-system fully functional since January 2015. We will now focus on the restauration of the disk drive.</b></a>. 
		
	
