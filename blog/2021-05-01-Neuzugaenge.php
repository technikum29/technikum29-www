<p>
Nach oberflächlicher Reinigung und etwas Ölen funktionierte die Maschine auf Anhieb in ihren Grundfunktionen. 
Einige Details sind noch im Zuge einer späteren Komplettüberholung zu erledigen. Einige fehlende Tastenkappen 
wurden uns freundlicherweise von Florian Ehmann per 3-D-Druck ersetzt (im Bild auf der linken Seite der Volltastatur 
zu sehen, Gravur fehlt noch).
  
<p>
Die Maschine wird wegen ihrer guten Zugänglichkeit bei Führungen zu mechanischen Rechenmaschinen vorgeführt werden.

<p>
Mit Sicherheit das schwerste der Neuzugänge ist eine komplette <strong>IBM5320 System/32</strong>. Sie wurde uns von Edwin Miller 
(Rammingen) gespendet. Die 5320 ist ein 16-Bit-Einzelplatzcomputer, der von 1975-1984 von IBM vertrieben (bzw. 
vermietet) wurde.

