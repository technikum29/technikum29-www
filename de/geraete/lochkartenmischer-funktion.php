<p>Im einfachsten Fall hat man nur zwei Kartenarten: Adresskarten und Umsatzkarten.
<br/>Die Adresskarten stehen in aufsteigender Folge nach Kundennummern in der Adresskartei. Die zunächst ungeordneten Umsatzkarten wurden durch die Sortiermaschine ebenfalls in aufsteigende Reihenfolge gebracht. Danach sind beide Kartenarten zu einem einzigen Kartenpaket so zu vereinigen, dass jeder Adresskarte die Umsatzkarten mit gleicher Kundennummer folgen. Liegen zu einer Adresskarte keine Umsatzkarte (oder umgekehrt) vor, so muss der Mischer diese Karte in ein gesondertes Fach ablegen.</p>

<p>Diese Arbeit, zwei bereits vorsortierte Kartenpakete zu einem einzigen zu vereinigen und unpaarige Karten auszusteuern, war eine der wichtigsten Aufgaben des Lochkartenmischers. Dazu hat ein Mischer im Allgemeinen 2 Zufuhrmagazine und 4 Ablagefächer. Es gab jedoch noch viele andere Anwendungsmöglichkeiten.</p>
