<p>Wir laden für
<strong>Sonntag, dem 27. Oktober 2019 um 15 Uhr</strong> in das technikum29 in
Kelkheim ein, um die Fortschritte seit dem Tod von Heribert Müller zu präsentieren,
sowie unsere Zukunftspläne zu diskutieren. Ziel ist es, Akteure und Interes-senten im Museum
zu versammeln, um auch gemeinsam über die Zukunft zu sprechen.

<p>
Wir wünschen uns, dass ihr eure Ideen mitbringt oder vorab per E-Mail mitteilt, wie esauch schon
