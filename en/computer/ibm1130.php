<h3 style="margin-left: 257px"> Restauration-Blog </h3>

			
			As for out UNIVAC 9200 we also maintain a repair blog for this machine. The main challenge in repairing a machine like this is that the machine was very advanced for its time. There are no individual transistors or common integrated circuits, instead IBM used a proprietary technology called SLT (Solid Logic Technology). SLT devices are small ceramic substrates on which extremely tiny transistors, diodes, resistors etc. are mounted (SLT devices are no integrated circuits - they more closely resemble hybrid circuits).<br>
			Of course, these devices are no longer manufactured, so there are virtually no spare parts. Accordingly, defective SLT devices must be "repaired" - a very tedious and time consuming process.<br>
			So the very advanced technology developed by IBM which made this machine feasible now turns out to be a real problem for its repair and restauration. Other companies, like BULL, still used discrete transistors mounted on classic printed circuit boards in the 1960 and some even later. 

The time required for a complex endeavor like this is substantial. Fortunately one never knows how complex the repair will be in the end (our BULL Gamma 55 was plagued by more than 30 distinct failures!) - otherwise one would never even begin such a repair attempt. The actual blog starts here and is continued on a follow-up page (see below).

	
