<p>Auch "von unten" kann sich das Gerät sehen lassen. Anstelle eines Chassis aus Blech wurde eine Hartpapierplatte verwendet. Die Verbindungen wurden teilweise durch aufgenietete Leiterbahnen realisiert. Ein Verfahren, welches erst Mitte der 50er Jahre wieder aufgegriffen wurde und zu den "gedruckten Schaltungen" führte. In dieser Hinsicht war der Empänger seiner Zeit weit voraus.</p>
