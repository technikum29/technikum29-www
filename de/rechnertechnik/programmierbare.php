    <h2>Beispiele für programmierbare Rechner der 2. Generation</h2>
	
    <p>Rechner der 2. Generation hatten noch keine integrierte Schaltkreise. Das erschwerte den Bau leistungsf&auml;higer Rechner erheblich. Daher sind programmierbare Rechner dieser Spezies technisch besonders interessant. Schon 1966 brachte die deutsche Firma DIEHL einen solchen Rechner auf den Markt. Das Betriebsprogramm wird &uuml;ber einen Metall-Lochstreifen "gebootet" (siehe "Speichermedien"), Anwenderprogramme auf Papier-Lochstreifen gestanzt, die wieder eingelesen werden k&ouml;nnen. Wahrlich die Steinzeit programmierbarer Rechner! Solche Komplett-Anlagen sind sehr rar.</p>
	
<h3>Diehl Combitron</h3><br>

<p>Der Diehl-Combitron war der erste funktionsfähige programmierbare Tischrechner, der in Deutschland gebaut und vertrieben wurde (1966-68).
Die ersten Combitron-Maschinen waren ohne Schnittstelle. Man hatte damit keine Möglichkeit, selbst geschriebene Programme zu speichern. Ein Programm konnte jedoch mit einem Zahlencode ausgedruckt werden, den man bei jedem "Neustart" über die Tastatur wieder eingeben musste. 

