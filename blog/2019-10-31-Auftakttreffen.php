<p>Zu den Gästen zählten unter anderem eine Vertretung des
<a href="https://www.mfk-frankfurt.de/">Museums für Kommunikation Frankfurt</a>, des
<a href="https://www.fitg.de/">Förderkreis Industrie und Technikgeschichte</a>, der
<a href="https://www.buergerstiftung-kelkheim.de/">Bürgerstiftung Kelkheim</a>, des
<a href="https://www.kelkheim.de/">Kulturreferats Kelkheim</a> sowie viele Personen,
die andere Sammlungen vertraten, unter anderem das
<a href="http://www.analogmuseum.org/">Analogmuseum</a> oder die
<a href="https://www.zdv.uni-mainz.de/computersammlung-zugaenglich/">Computersammlung
Mainz</a> und viele mehr. Einige unserer Gäste kamen aus der gleichen Straße, andere
sind weit gereist: Vier WANG-Experten nahmen 150-250km Weg auf sich. Manch ein Gast
kam aus Hamburg oder Münster in Westfalen zum Auftakttreffen.

<h3>Zwei Stunden im technikum</h3>
<p>Der Nachmittag wurde von Sven geleitet, der zunächst die Ehre hatte, auch die anderen
anwesenden Erben vorzustellen und die Erbengemeinschaft zu repräsentieren. Im
Rahmen <a href="/auftakt/Präsentation Auftakt 2019.pdf">einer Präsentation</a> stellte
er zunächst die Person <a href="/heribert-mueller/">Heribert Müller</a>, seine
Techniksammlung und die Gründung des technikum29 im November 2005 vor. Außerdem erzählte
er, was seitdem alles passiert ist: Von den Vortragsreihen, den
Elektronik-Entwicklungen, LEGO-Roboter-Workshops, Kunst und Kultur im Museum,
der Öffentlichkeitswahrnehmung in den Medien und vielem mehr.

<p>Anschließend ging es, dem Besucheransturm geschuldet, in zwei Gruppen durch die
Sammlung. Hierbei setzten Roland und Sven jeweils Akzente, berichteten über erfolgte
Restaurationen oder den allgemeinen Zustand sowie der Zukunft einzelner Themenbereiche.

<p>Den dritten Teil des Treffens bildete erneut eine Präsentation
(<a href="/auftakt/Präsentation Auftakt 2019.pdf#page=32">ab Slide 32</a>), in der die
erfolgte Aufarbeitung seit Sommer 2018 vorgestellt wurde. Wichtig war es den
Veranstaltern auch, den virtuellen Blick in die Lagerräume zu ermöglichen. Sven hat
dann betont, worum es ihm bei der Museumszukunft vor allem geht, und welche
organisatorischen Vorarbeiten geleistet wurden. Die Präsentation mündete in einer
Diskussion, in der zahlreiche Besucher weitere Aspekte beitrugen.

<h3>Was wir vom Treffen mitnehmen</h3>
<p>Wir haben nicht nur zwei Plakate mit Anregungen und Expertisen unserer Teilnehmer
ausfüllen lassen, sondern viele Tipps und Ideen erhalten, die wir uns vor einer
Vereinsgründung noch einmal durch den Kopf gehen lassen.

<p>Mit den Interessenten bleiben wir per E-Mail, über unseren Blog sowie möglicherweise
per Forum in Berührung. Wir probieren's auch als
<a href="https://twitter.com/technikum29">@technikum29</a> auf Twitter.

<p>Die nächsten Schritte für uns sind derzeit: Rechtliche und steuerliche Klärung
der Vereinsmodalitäten.






