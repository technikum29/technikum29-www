<p>
In den letzten Monaten erreichten das technikum29 eine Vielzahl von interessanten neuen Exponaten, 
die wir hier kurz vorstellen möchten:

<p>
Das älteste Exponat übereignete uns freundlicherweise Frau Sigrun Schuster aus Kronberg: eine Staffelwalzen-
Rechenmaschine Typ <strong> „XxX“ von Seidel und Naumann</strong> (Dresden). Dieses Modell wurde von 1906 bis 1927 hergestellt, 
eine genauere Zuordnung des Herstelljahrs zu unserer Maschine ist uns im Augenblick nicht möglich.

