<h2>We are looking for patrons</h2>

<p>After the sudden pass of <a href="/heribert-mueller/">Heribert Müller</a>
in 2018, the museum is about to be start from scratch. We are working hard
to prepare the foundation of a non-profit society (<em>Gemeinnütziger
Verein</em> in German, a legal entity). We are looking for support of any
kind: It doesn't even matter where you are located and whether you can
donate time or money.

<h3>Support in person: Get an associate</h3>
<p>Many of us live far away from the Rhine-Maine metropolitan area, where the
museum is located. Some of us come from southern Germany, some from northern
Germany. Many of us have to travel half a day to reach the museum. Thankfully,
Frankfurt is well-connected in terms of rail traffic and it's large
international airport.

<p>Nevertheless, we maintain a large website and
<a href="https://github.com/technikum29">a lot of code</a>. We always look for
people willing to contribute for hosting, authoring and writing, doing artwork,
public outreach, writing mails, or creating relationships with other institutes.

<p>Currently, we maintain a list of people interested in shaping the future
of the museum, as well as just getting informed regularly about updates from
our house. <a href="/en/contact.php">Drop us a mail</a> if you want to get put
on the list.

<h3>Financial support: Donations</h3>
